private boolean validarCampos() {
    if (txtNome.getText().trim().isEmpty() ||
        txtPais.getText().trim().isEmpty() ||
        txtEstado.getText().trim().isEmpty() ||
        txtCidade.getText().trim().isEmpty() ||
        txtBairro.getText().trim().isEmpty() ||
        txtRua.getText().trim().isEmpty() ||
        txtNumero.getText().trim().isEmpty() ||
        txtCep.getText().trim().isEmpty()) {
            
        JOptionPane.showMessageDialog(this, "Preencha todos os campos.");
        return false;
    }
    return true;
}

private void limparCampos() {
    txtNome.setText("");
    txtPais.setText("");
    txtEstado.setText("");
    txtCidade.setText("");
    txtBairro.setText("");
    txtRua.setText("");
    txtNumero.setText("");
    txtCep.setText("");
}
 
private void configurarTabela() {
    tableModel = new DefaultTableModel(
        new Object[]{"ID", "Nome", "País", "Estado", "Cidade", "Bairro", "Rua", "Número", "CEP"}, 0
    );
    jTable1.setModel(tableModel);
    jTable1.getColumnModel().getColumn(0).setMinWidth(0);
    jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
    jTable1.getColumnModel().getColumn(0).setWidth(0);
}

private void carregarDadosTabela() {
    tableModel.setRowCount(0); 
    List<Garagem> garagens = garagemDAO.read();
    for (Garagem g : garagens) {
        tableModel.addRow(new Object[]{
            g.getId(), g.getNome(), g.getPais(), g.getEstado(), 
            g.getCidade(), g.getBairro(), g.getRua(), g.getNumero(), g.getCep()
        });
    }
}


