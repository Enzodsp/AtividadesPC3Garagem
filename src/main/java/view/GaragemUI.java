    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    Garagem garagem = new Garagem();
    garagem.setNome(txtNome.getText());
    garagem.setPais(txtPais.getText());
    garagem.setEstado(txtEstado.getText());
    garagem.setCidade(txtCidade.getText());
    garagem.setBairro(txtBairro.getText());
    garagem.setRua(txtRua.getText());
    garagem.setNumero(txtNumero.getText());
    garagem.setCep(txtCep.getText());

    garagemDAO.create(garagem);
    limparCampos();
    carregarDadosTabela();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int row = jTable1.getSelectedRow();
    if (row < 0) {
        JOptionPane.showMessageDialog(this, "Selecione uma garagem para editar.");
        return;
    }
    
    if (validarCampos()) {
        try {
            int id = (int) tableModel.getValueAt(row, 0);
            Garagem garagem = new Garagem();
            garagem.setId(id);
            garagem.setNome(txtNome.getText());
            garagem.setPais(txtPais.getText());
            garagem.setEstado(txtEstado.getText());
            garagem.setCidade(txtCidade.getText());
            garagem.setBairro(txtBairro.getText());
            garagem.setRua(txtRua.getText());
            garagem.setNumero(txtNumero.getText());
            garagem.setCep(txtCep.getText());

            garagemDAO.update(garagem);
            JOptionPane.showMessageDialog(this, "Garagem atualizada com sucesso!");
            limparCampos();
            carregarDadosTabela();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar garagem: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
    int row = jTable1.getSelectedRow();
    if (row < 0) {
        JOptionPane.showMessageDialog(this, "Selecione uma garagem para excluir.");
        return;
    }
    int id = (int) jTable1.getValueAt(row, 0);

    int confirm = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir esta garagem?");
    if (confirm == JOptionPane.YES_OPTION) {
        garagemDAO.delete(id);
        JOptionPane.showMessageDialog(this, "Garagem excluída com sucesso!");
        limparCampos();
        carregarDadosTabela();
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

